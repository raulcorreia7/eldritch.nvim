local function extend_palette(base, overrides)
  local palette = vim.tbl_deep_extend("force", {}, base, overrides or {})
  palette.git = vim.tbl_deep_extend("force", {}, base.git or {}, palette.git or {})
  palette.gitSigns = vim.tbl_deep_extend("force", {}, base.gitSigns or {}, palette.gitSigns or {})
  return palette
end

